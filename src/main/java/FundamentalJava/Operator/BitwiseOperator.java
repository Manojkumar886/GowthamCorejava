package FundamentalJava.Operator;

public class BitwiseOperator
{
    public static void main(String[] args)
    {
        int a=51;
        int b=71;
        System.out.println("a & b value="+(a&b));//AND bitwise Operator
        System.out.println("a | b value="+(a|b));//OR bitwise Operator
        System.out.println("a ^ b value="+(a^b));//XOR bitwise Operator
    }
}
