package FundamentalJava.Operator;

public class ShiftOperator
{
    public static void main(String[] args)
    {
//        right shift>>,left shift <<
        int a=100;
        int b=5;

        System.out.println(a>>b);  // a/2 (b)
//        100 /32  =3

        System.out.println(a<<b);  // a* 2b
       //       100*32



    }

}
